package _01_Uebungen_Einfuehrung;

class Person {

    String vorname;
    String nachname;
    int alter;
    String geschlecht;

    public Person(String vorname, String nachname, int alter, String geschlecht) {
        this.vorname = vorname;
        this.nachname = nachname;
        this.alter = alter;
        this.geschlecht = geschlecht;
    }
}
